#include <stdlib.h>
#include <openssl/evp.h>
#include <openssl/aes.h>

char key[] = {0xca, 0xfc, 0xfc, 0x0d, 0x81, 0x43, 0x62, 0xbf, 0xe4, 0xf7, 0xb9, 0xf1, 0xf1, 0xbf, 0x77, 0xd8, 0x0a, 0x9e, 0xeb, 0xc0, 0x1d, 0x48, 0x2f, 0x92, 0xde, 0xef, 0xfb, 0x16, 0x85, 0x19, 0xd8, 0x91};
char iv[] = {0xAF, 0xF2, 0xEB, 0x2C, 0xA9, 0x80, 0xE9, 0x28, 0xB4, 0xC3, 0xBD, 0xB5};
unsigned char data[] = {0x89,0x86,0x9D,0xB8,0x19,0x07,0xC4,0xEC,0xE6,0x9C,0x60,0xD8,0x4A,0x65,0x55,0x80,0x8F,0x97,0xAD,0x68,0x85,0xA0,0x0A,0x3A,0x33,0xC3,0xF2,0x91,0x46,0x56,0x21,0x4D};

int main() {
  EVP_CIPHER_CTX *ctx = EVP_CIPHER_CTX_new();
  int p_len = 0x20;
  int len;
  
  unsigned char *plaintext = malloc(0x100);
  
  EVP_CIPHER_CTX_init(ctx);
  EVP_DecryptInit_ex(ctx, EVP_aes_256_gcm(), NULL, key, iv);
  EVP_DecryptUpdate(ctx, plaintext, &p_len, data, sizeof(data));
  EVP_DecryptFinal_ex(ctx, (unsigned char*)(plaintext + p_len), &len);

  printf("%s\n", plaintext);

  EVP_CIPHER_CTX_free(ctx);
  return 0;
}
